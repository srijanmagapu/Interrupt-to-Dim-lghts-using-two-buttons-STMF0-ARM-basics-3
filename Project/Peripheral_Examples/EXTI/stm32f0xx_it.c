/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)    //interrupt handler that runs once in 5 ms
{

   extern volatile int c;       // external int c volatile
    extern int dim;             //external int dim        
  extern volatile unsigned short Var;   //short Var

   if (c++ == 0){               //if c is 0 increment c
     
      STM_EVAL_LEDOff (LED3);   //turnoff led3
      STM_EVAL_LEDOff (LED4);   //turnoff led4
    }else if (c >= dim){        //if c>=dim

      STM_EVAL_LEDOn (LED3);    //turnon led3
      STM_EVAL_LEDOn (LED4);    //turnon led4
      c =0;                     //make c 0 when it is greater than dim and again start incrementing until is equal to dim
    }

    Var = (Var<<1);             //left shift Var value
    if (GPIO_ReadInputDataBit(GPIOA, GPIO_Pin_0)){      //if button is pressed

      Var |= 1;                                         //or the Var with 1
  }       
}

